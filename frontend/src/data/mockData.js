// Helper function to get paginated recipes
export const getPaginatedRecipes = (page = 1, limit = 10, filters = {}) => {
    let filtered = [...mockRecipes];

    // Apply filters
    if (filters.title) {
        filtered = filtered.filter(recipe =>
            recipe.title.toLowerCase().includes(filters.title.toLowerCase())
        );
    }

    if (filters.cuisine) {
        filtered = filtered.filter(recipe =>
            recipe.cuisine.toLowerCase().includes(filters.cuisine.toLowerCase())
        );
    }

    if (filters.rating) {
        filtered = filtered.filter(recipe => recipe.rating >= filters.rating);
    }

    if (filters.total_time) {
        filtered = filtered.filter(recipe => recipe.total_time <= filters.total_time);
    }

    if (filters.calories) {
        filtered = filtered.filter(recipe => {
            const cal = parseInt(recipe.nutrients.calories);
            return cal <= filters.calories;
        });
    }

    // Sort by rating descending
    filtered.sort((a, b) => b.rating - a.rating);

    // Calculate pagination
    const totalRecipes = filtered.length;
    const totalPages = Math.ceil(totalRecipes / limit);
    const startIndex = (page - 1) * limit;
    const endIndex = startIndex + limit;
    const recipes = filtered.slice(startIndex, endIndex);

    return {
        page,
        limit,
        total_recipes: totalRecipes,
        total_pages: totalPages,
        recipes
    };
};
